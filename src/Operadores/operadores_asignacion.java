package Operadores;

public class operadores_asignacion {
    public static void main(String[] args) {
        int no1 = 10;
        no1 = no1 + 5;
        no1 += 32; // no1 = no1 + 32
        no1 -= 10;
        no1 *= 3;
        no1 /= 3;
        no1 %= 3;
        System.out.println("no1 = " + no1);
    }
}
