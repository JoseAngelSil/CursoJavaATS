package Introduccion;

public class Constantes {
	public static void main(String[] args) {
		final int no1 = 23; // Declaracion de una constante en java con la palabra final
		System.out.println("no1 = " + no1);
		
		var nombre = "Jose Angel"; // Creacion de variables dinamicas con var.
		System.out.println(nombre);
	}
}
